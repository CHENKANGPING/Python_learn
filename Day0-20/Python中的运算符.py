# 比较运算符和逻辑运算符
# and: True and True = True, False and True = False, True and False = Flase, Flase and Flase = Flase
# or: True or False = True, False or True = True
# not: True = False, False = True
flag0 = 1 == 1
flag1 = 3 > 2
flag2 = 2 < 1
flag3 = flag1 and flag2
flag4 = flag1 or flag2
flag5 = not flag0
