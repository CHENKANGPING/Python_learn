# 运算符和表导式的应用
# 1. 华氏温度转摄氏温度
# 要求:输入华氏温度将其转换成摄氏温度.公式:C = (F - 32) / 1.8
a = float(input('输入华氏温度: '))
b = (a - 32) / 1.8
print('%.1f华氏度 = %.1f摄氏度' % (a, b))
# print 输出的字符串中有两个 %.1f占位符,这两个占位符会被 %之后的(a, b) 中的两个float类型的内容给替换掉, '1' 为保留1位有效数字
# %d 占位符 用int类型替换, %s 占位符 用str类型替换

f = float(input('请输入华氏温度: '))
c = (f - 32) / 1.8
print(f'{f:.1f}华氏度 = {c:.1f}摄氏度')
# Python 中还可以用下面的办法来格式化输出，我们给出一个带占位符的字符串，字符串前面的f表示这个字符串是需要格式化处理的，
# 其中的{f:.1f}和{c:.1f}可以先看成是{f}和{c}，表示输出时会用变量f和变量c的值替换掉这两个占位符，
# 后面的:.1f表示这是一个浮点数，小数点后保留1位有效数字。

print('---------------------------------------------------------------')

# 2. 计算圆的周长和面积
# 要求:输入半径算出周长和面积
radius = float(input('请输入圆的半径: '))
circumference = 2 * 3.1416 * radius
Area = 3.1416 * radius * radius
print('周长: %.2f' % circumference)
print('面积: %.2f' % Area)

print('---------------------------------------------------------------')

